    public void cargarTabla(ArrayList<Cancion> cancion) {
       DefaultTableModel artista = (DefaultTableModel) jTable2.getModel();
       artista.setRowCount(0); // Limpia la tabla antes de agregar nuevos datos
       
        artista.addColumn("ID");
        artista.addColumn("TITULO");
        artista.addColumn("ARTISTA");
        artista.addColumn("AÃ‘O");
        artista.addColumn("GENERO");
        artista.addColumn("DURACION");
   

        for (Cancion Cancion : Cancion) {
            artista.addRow(new Object[]{cancion.getId(), cancion.getTitulo(), cancion.getArtista(), cancion.getAnio(),cancion.getGenero(), cancion.getDuracion()});
        }
        
        artista.fireTableDataChanged();

        jTable2.setModel(artista);
        jTable2.revalidate();
        jTable2.repaint();
    }



